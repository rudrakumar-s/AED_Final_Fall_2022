                if(!matcher3.matches())
                {
                    JOptionPane.showMessageDialog(this,"Enter a Valid Fine Amount!");
                    flag = 1;
                    TxtFine.setText("");
                    
                    
                }
                String cellPhoneNumber2 = TxtDelay.getText();
                Pattern pattern2 = Pattern.compile("^[0-9]{3}$");
                Matcher matcher2 = pattern2.matcher(cellPhoneNumber2);
